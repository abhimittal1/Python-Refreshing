"""
Python has several built-in data types used to classify data. These include: 

Numeric:
    int: Represents integers (e.g., -3, 0, 5).
    float: Represents floating-point numbers (e.g., 3.14, -0.5).
    complex: Represents complex numbers (e.g., 2 + 3j).

Sequence Types:
    str: Represents strings, which are sequences of characters (e.g., "hello", 'world').
    list: Represents ordered, mutable sequences of items (e.g.,, ['a', 'b', 'c']).
    tuple: Represents ordered, immutable sequences of items (e.g., (1, 2, 3), ('a', 'b', 'c')).
    range: Generates a sequence of numbers within a specified range.

Mapping Type:
    dict: Represents key-value pairs, where keys are unique and immutable (e.g., {'name': 'Alice', 'age': 30}).

Set Types:
    set: Represents unordered collections of unique items (e.g., {1, 2, 3}, {'a', 'b', 'c'}).
    frozenset: Represents immutable sets.

Boolean Type:
    bool: Represents truth values, either True or False.

Binary Types:
    bytes: Represents immutable sequences of bytes.
    bytearray: Represents mutable sequences of bytes.
    memoryview: Provides a memory view of an object without copying.

None Type:
    NoneType: Represents the absence of a value, denoted by None.

"""

